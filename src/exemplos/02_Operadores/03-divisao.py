# Exemplos de utilização do operador de divisão. Em Python, só é possível
# dividir valores numéricos.


print('Divisão:')

# Escreva o resultado da operação 3 / 2.
print('      3 / 2   = %f' % (3 / 2))

# Escreva o resultado da operação 2 / 3.
print('      2 / 3   = %f' % (2 / 3))

# Escreva o resultado da operação 2 / 3.0.
print('      2 / 3.0 = %f' % (2 / 3.0))

# Escreva o resultado da operação 2.0 / 3.
print('    2.0 / 3   = %f' % (2.0 / 3))

# Escreva o resultado da operação 2.0 / 3.0.
print('    2.0 / 3.0 = %f' % (2.0 / 3.0))

# Divisão por zero (0) não é uma operação válida!
# print('    2.0 / 0.0 = %f' % (2.0 / 0.0))


# O operador //  serve para realização da divisão inteira, ou seja, o resultado
# é um valor inteiro.

# Escreva o resultado da operação 3 // 2.
print('      3 // 2   = %d' % (3 // 2))

# Escreva o resultado da operação 2 // 3.
print('      2 // 3   = %d' % (2 // 3))

# Escreva o resultado da operação 2 // 3.0.
print('      2 // 3.0 = %d' % (2 // 3.0))

# Escreva o resultado da operação 2.0 // 3.
print('    2.0 // 3   = %d' % (2.0 // 3))

# Escreva o resultado da operação 2.0 // 3.0.
print('    2.0 // 3.0 = %d' % (2.0 // 3.0))
